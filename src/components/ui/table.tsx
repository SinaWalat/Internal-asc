const TableRow = React.forwardRef<
  HTMLTableRowElement,
  React.HTMLAttributes<HTMLTableRowElement>
>(({ className, style, onMouseMove, onMouseEnter, onMouseLeave, ...props }, ref) => {
  const [position, setPosition] = React.useState({ x: 0, y: 0 });
  const [opacity, setOpacity] = React.useState(0);

  const handleMouseMove = (e: React.MouseEvent<HTMLTableRowElement>) => {
    const row = e.currentTarget;
    const rect = row.getBoundingClientRect();
    setPosition({ x: e.clientX - rect.left, y: e.clientY - rect.top });
    onMouseMove?.(e);
  };

  const handleMouseEnter = (e: React.MouseEvent<HTMLTableRowElement>) => {
    setOpacity(1);
    onMouseEnter?.(e);
  };

  const handleMouseLeave = (e: React.MouseEvent<HTMLTableRowElement>) => {
    setOpacity(0);
    onMouseLeave?.(e);
  };

  return (
    <tr
      ref={ref}
      onMouseMove={handleMouseMove}
      onMouseEnter={handleMouseEnter}
      onMouseLeave={handleMouseLeave}
      className={cn(
        "border-b transition-colors hover:bg-transparent data-[state=selected]:bg-muted relative",
        className
      )}
      style={{
        ...style,
        backgroundImage: `radial-gradient(600px circle at ${position.x}px ${position.y}px, rgba(249, 115, 22, ${0.15 * opacity}), transparent 40%)`
      }}
      {...props}
    />
  );
})
TableRow.displayName = "TableRow"

